package ImpJava.Operators;

public class AritmeticOperators {
    public static void main(String [] args){
        int a=30;
        int b =20;
        int addition = a+b;
        int Subtraction = a-b;
        int Multiplication = a*b;
        int Division = a/b;
        int Modulu = a%b;
        int Incerement = ++ a;
        int Decrement = --b;

        System.out.println("Addition this program " + addition);
        System.out.println("Subtraction this program "+ Subtraction);
        System.out.println("Multiplication this program " + Multiplication);
        System.out.println("Division this program " +Division);
        System.out.println("Modulus this program " +Modulu);
        System.out.println("Incerment this program "+ Incerement);
        System.out.println("Decrement this program "+Decrement);
    }
}
