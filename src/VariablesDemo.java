public class VariablesDemo {
    public static void main(String[] args) {
        int myIntNumber = 234;
        float myFloatNumber = 40.34f;
        boolean myBooleanNumber = true;
        double MyDoubleNumber = 4444.444f;
        char myCharNumber = 'P';
        String myStringNumber = "Partha Majumder";

        System.out.println(myStringNumber);
        System.out.println(myCharNumber);
        System.out.println(MyDoubleNumber);
        System.out.println(myBooleanNumber);
        System.out.println(myFloatNumber);
        System.out.println(myIntNumber + "\n");

        int X, Y, Z;
        X=Y=Z=50;
        System.out.println(X+Y+Z);


    }
}
